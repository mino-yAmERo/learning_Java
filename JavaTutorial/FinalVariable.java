public class FinalVariable {
        public static void main(String[] args) {
            final int NUM1; //final กำหนดค่าคงที่ (เปลี่ยนแปลงไม่ได้)
            final int NUM2;//final กำหนดค่าคงที่ (เปลี่ยนแปลงไม่ได้)
            //ค่าคงที่ ให้เขียนตัวพิมพ์ใหญ่ เช่น NUM1, NUM2
            NUM1=10;
            NUM2=20;
            System.out.println("ค่าตัวเลขที่ 1 = " +NUM1);
            System.out.println("ค่าตัวเลขที่ 2 = " +NUM2);
        }
    }
    

