public class Prepost {
    public static void main(String[] args) {
        int a=5;
        // pre =หน้า | post= หลัง
        System.out.println(a);
        System.out.println(++a);
        System.out.println(a);

        a=5;
        System.out.println(a);
        System.out.println(a++);
        System.out.println(a);

        int b=5;
        System.out.println(b);
        System.out.println(--b);
        System.out.println(b);

        b=5;
        System.out.println(b);
        System.out.println(b--);
        System.out.println(b);

    }
}
