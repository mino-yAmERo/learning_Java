public class comparetutorial {
    public static void main(String[] args) {
        // true / false
        int a =10, b=5;
        boolean c= (a==b);
        System.out.println(a+" equal "+b+ " ? = " +c);
        System.out.println(a+" not equal "+b+ " ? = " +(a!=b));
        System.out.println(a+" is less than "+b+ " ? = " +(a<b));
        System.out.println(a+" is greater than "+b+ " ? = " +(a>b));
        System.out.println(a+" is less than "+b+ " or equal to " +b+ " ? = " +(a<=b));
        System.out.println(a+" is greater than "+b+ " or equal to " +b+ " ? = " +(a>=b));

    }
}
